    private void showPanel(boolean pBeranda, boolean pEmas, boolean pData, boolean pPelatihan, 
            boolean pPengujian, boolean pReport){
        Color colorAsli = new Color(33,79,80);
        Color colorPilih = new Color(68, 106, 108);
        resizeImage.tampilImg(lbIconMenuBeranda, "/beranda/beranda putih.png");
        resizeImage.tampilImg(lbIconMenuEmas, "/emas/emas putih.png");
        resizeImage.tampilImg(lbIconMenuDataLatihUji, "/data/data putih.png");
        resizeImage.tampilImg(lbIconMenuPelatihan, "/ai/ai putih.png");
        resizeImage.tampilImg(lbIconMenuPengujian, "/ai/ai putih.png");
        resizeImage.tampilImg(lbIconMenuRiwayat, "/report/report putih.png");
        if(pBeranda){
            this.pBeranda.setVisible(true);
            this.pMenuBeranda.setBackground(colorPilih);
            resizeImage.tampilImg(lbIconPath, "/beranda/beranda hitam.png");
            lbPath.setText("/ "+lbMenuBeranda.getText());
        }else{
            this.pBeranda.setVisible(false);
            this.pMenuBeranda.setBackground(colorAsli);
        }
        
        if(pEmas){
            this.pEmas.setVisible(true);
            this.pMenuEmas.setBackground(colorPilih);
            resizeImage.tampilImg(lbIconPath, "/emas/emas hitam.png");
            lbPath.setText("/ "+lbMenuEmas.getText());
        }else{
            this.pEmas.setVisible(false);
            this.pMenuEmas.setBackground(colorAsli);
        }
        
        if(pData){
            this.pDataLatihUji.setVisible(true);
            this.pMenuDataLatihUji.setBackground(colorPilih);
            resizeImage.tampilImg(lbIconPath, "/data/data hitam.png");
            lbPath.setText("/ "+lbMenuDataLatihUji.getText());
        }else{
            this.pDataLatihUji.setVisible(false);
            this.pMenuDataLatihUji.setBackground(colorAsli);
        }
        
        if(pPelatihan){
            this.pPelatihan.setVisible(true);
            this.pMenuPelatihan.setBackground(colorPilih);
            resizeImage.tampilImg(lbIconPath, "/ai/ai hitam.png");
            lbPath.setText(" / "+lbMenuPelatihan.getText());
        }else{
            this.pPelatihan.setVisible(false);
            this.pMenuPelatihan.setBackground(colorAsli);
        }
        
        if(pPengujian){
            this.pPengujian.setVisible(true);
            this.pMenuPengujian.setBackground(colorPilih);
            resizeImage.tampilImg(lbIconPath, "/ai/ai hitam.png");
            lbPath.setText(" / "+lbMenuPengujian.getText());
        }else{
            this.pPengujian.setVisible(false);
            this.pMenuPengujian.setBackground(colorAsli);
        }
        
        if(pReport){
            this.pReport.setVisible(true);
            this.pMenuReport.setBackground(colorPilih);
            resizeImage.tampilImg(lbIconPath, "/report/report hitam.png");
            lbPath.setText(" / "+lbMenuReport.getText());
        }else{
            this.pReport.setVisible(false);
            this.pMenuReport.setBackground(colorAsli);
        }
    }
    
