    private void lbNorDenorPelatihanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbNorDenorPelatihanMouseClicked
        // TODO add your handling code here:
        double[][] dataTrain = modelPelatihan.dataTrain(this);
        if(lbNorDenorPelatihan.getText().equals("Normalisasi")){
            double[][] norDataTrain = bp.normaslisasi(dataTrain);
            modelPelatihan.tampilDataTrain(this, norDataTrain);
            lbNorDenorPelatihan.setText("Denormalisasi");
        }else{
            double[][] norDataTrain = bp.normaslisasi(dataTrain);
            double[][] denorDataTrain = bp.denorMasal(norDataTrain, dataTrain);
            modelPelatihan.tampilDataTrain(this, denorDataTrain);
            lbNorDenorPelatihan.setText("Normalisasi");
        }
    }//GEN-LAST:event_lbNorDenorPelatihanMouseClicked

